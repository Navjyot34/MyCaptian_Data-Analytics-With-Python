# Python Program to Illustrate Different Set Operations Here we have to define two set variables and we have to perform different set operations: union, intersection, difference and symmetric difference.

  

A = {0, 2, 4, 6, 8};
B = {1, 2, 3, 4, 5};
  
print("Union of A and B :", A | B)
  
print("Intersection of A and B :",  A & B)
  
print("Difference of A and B :", A - B)
  
print("Symmetric difference of A and B :", A ^ B)
